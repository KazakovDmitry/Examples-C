void PrintImage (int [,] image) //метод печати массива
{
    for (int i = 0; i < image.GetLength(0); i++)  //цикл по строкам, GetLength(0) - означает количество строк
    {
        for (int j = 0; j < image.GetLength(1); j++)  //цикл по столбцам, GetLength(1) - означает количество столбцов
        {
            if(image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}
